package week_4;

public class ForLoop3 {

	public static void main(String[] args) {
		
		
		// Create a for loop between 5 to 100(included)
		// Print out all the elements
		// Increase the value by 10 
		// 5 - 15 - 25 - 35 - ... - 105
		
		for (int i = 5; i <= 105; i+=10) {
			System.out.print(i + " - ");
		}
		

	}

}
