package week_2;

public class CharOperators {

	public static void main(String[] args) {

		// Char is characters
		char letter = 'A';
		System.out.println(letter);

		int ascii = letter;
		System.out.println(ascii);

		char numeric = 65;
		System.out.println(numeric);

		char myChar = 'B';
		System.out.println(myChar);

		int myNumber = ++myChar;
		System.out.println(myChar);
		System.out.println(myNumber);

	}

}
